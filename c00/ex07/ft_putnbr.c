#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	s[10];
	int		i;
	int		mod;

	if (nb < 0)
		write(1, "-", 1);
	i = 0;
	while (nb != 0)
	{
		mod = nb % 10;
		if (mod < 0)
			mod *= -1;
		s[i] = mod + '0';
		nb /= 10;
		i++;
	}
	i--;
	if (i < 0)
		write(1, "0", 1);
	while (i >= 0)
	{
		write(1, &s[i], 1);
		i--;
	}
	return ;
}
